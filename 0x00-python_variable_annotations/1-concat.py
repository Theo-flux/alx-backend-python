#!/usr/bin/env python3
"""
concat.py file
"""


def concat(str1: str, str2: str) -> str:
    """
    a type-annotated function concat that takes a
    string str1 and a string str2 as arguments and
    returns a concatenated string

    Args:
        str1 (str): first string param
        str2 (str): second string param

    Returns:
        str: output
    """

    return f"{str1}{str2}"
